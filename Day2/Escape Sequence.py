# Escape sequence characters are used to represent certain special characters in a string

# \n - Newline
print("Hello\nWorld")

# \t - Tab space
print("Name:\tRoshan")

# \' - Single quote inside single quotes
print('It\'s a beautiful day')

# \" - Double quote inside double quotes
print("He said, \"Hello!\"")

# \\ - Backslash
print("This is a backslash: \\")

# \r - Carriage return (brings cursor to the beginning)
print("12345\rABCD")

# \b - Backspace (removes one character)
print("Helloo\b")