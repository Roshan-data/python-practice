# Python has different types of data (data types)

# String (text)
name = "Roshan"
print(name, "is of type", type(name))

# Integer (whole numbers)
age = 20
print(age, "is of type", type(age))

# Float (decimal numbers)
height = 5.9
print(height, "is of type", type(height))

# Boolean (True or False)
is_student = True
print(is_student, "is of type", type(is_student))

# Complex Number
num = 2 + 3j
print(num, "is of type", type(num))

# List (ordered collection)
fruits = ["apple", "banana", "cherry"]
print(fruits, "is of type", type(fruits))

# Tuple (ordered, immutable collection)
coordinates = (10, 20)
print(coordinates, "is of type", type(coordinates))

# Set (unordered, no duplicates)
unique_numbers = {1, 2, 3, 3}
print(unique_numbers, "is of type", type(unique_numbers))

# Dictionary (key-value pairs)
person = {"name": "Roshan", "age": 20}
print(person, "is of type", type(person))