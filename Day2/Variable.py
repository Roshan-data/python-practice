# A variable is a name used to store a value
# It is like a labeled box where you store something

# Assigning values to variables
name = "Roshan"
age = 20

# You can print the values stored in variables
print("My name is:", name)
print("My age is:", age)

# Variables can be updated (reassigned)
age = 21
print("Updated age is:", age)

# You can assign one value to multiple variables
x = y = z = "Python"
print(x, y, z)
